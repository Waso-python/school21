#include "libft.h"

int	ft_isalnum(int sym)
{
	if (sym >= 65 && sym <= 90)
		return (1);
	if (sym >= 97 && sym <= 122)
		return (1);
	if (sym <= 57 && sym >= 48)
		return (1);
	return (0);
}
